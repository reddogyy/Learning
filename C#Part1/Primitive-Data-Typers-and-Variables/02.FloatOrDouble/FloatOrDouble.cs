using System;

class FloatOrDouble
{
    static void Main(string[] args)
    {

        float floatValue1 = 12.345F;
        float floatValue2 = 3456.091F;
        double doubleValue1 = 34.567839023;
        double doubleValue2 = 8923.1234857;
        Console.WriteLine("Float values are :" + floatValue1 + "\n" + floatValue2 + "\n" + "Double values are :" + doubleValue1 + "\n" + doubleValue2);

    }
}

