using System;
class DeclareVariables
{
    static void Main(string[] args)
    {
        int i = 4825932;
        uint ui = 4825932;
        byte b = 97;
        sbyte sb = -115;
        short s = -10000;
        ushort us = 52130;
        long l = 4825932;
        ulong ul = 4825932;
        Console.WriteLine("int =" + i + "\n" + "uint = " + ui + "\n" + "byte = " + b + "\n" + "sbyte = " + sb + "\n");
        Console.WriteLine("short = " + s + "\n" + "ushort = " + us + "\n" + "long = " + l + "\n" + "ulong = " + ul);
    }
}
